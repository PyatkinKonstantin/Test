package com.company;

public class Operations {
    public static int div(int a, int b) {
        int c = a / b;
        return c;
    }
    public static int mult(int a, int b) {
        int c = a * b;
        return c;
    }
    public static int sub(int a, int b) {
        int c = a - b;
        return c;
    }
    public static int summa(int a, int b) {
        int c = a + b;
        return c;
    }
}
