package com.company;

public class Operations {
    public static int div(int a, int b) {
        return a / b;
    }
    public static int mult(int a, int b) {
        return a * b;
    }
    public static int sub(int a, int b) {
        return a - b;
    }
    public static int summa(int a, int b) {
        return a + b;
    }
}
